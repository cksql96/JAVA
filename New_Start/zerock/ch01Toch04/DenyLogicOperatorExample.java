package ch01Toch04;

public class DenyLogicOperatorExample {
	
	public static void main(String[] args) {
		boolean play = true;
		System.out.println(play);
		
		//---------------------------//
		play = !play;				//Toggle
		System.out.println(play);
		
		//-------------------------//
		
		play = !play;
		System.out.println(play);
		
		boolean isStopped = false;
		while(!isStopped) {
			;;
		}	//while
		
		
		
	}	//end main

}	//end class

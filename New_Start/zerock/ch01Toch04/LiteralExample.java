package ch01Toch04;

public class LiteralExample {
	//필드 선언영역
	
	//생성자 또는 메소드 영역은 빈행으로 구분시켜 줍니다.
	public static void main(String[] args) {
		int var1 = 10;						//숫자리터럴(10진수) -리터럴 순수한값.
		System.out.println(var1);
		
		int var2 = 010;						//숫자리터럴(8진수)
		System.out.println(var2);
		
		int var3 = 0x10;					//숫자리터럴(16진수)
		System.out.println(var3);
		
		//------------------------//
				
	}	//main

}	//end class

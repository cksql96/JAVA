package ch01Toch04;

public class WhilePrintFrom1To10Example {
	
	
	public static void main(String[] args) {
		int i = 1;
		
		while(i<=10) {
			System.out.print(i + "   ");
			
			i++;
		}	//end while
		
		//---------------------------------------
		System.out.println();
		
		for(int j = 1;j <= 10;j++) {
			System.out.print(j + "   ");			
		}	//end for
		
		
		
	}	//end main

}	//end class
